fun main(args: Array<String>) {
    val floatLiteral: Float = 178.95F //Literal Float
    println("Contoh Literal Float : " + floatLiteral)

    val oneBillion: Long = 1000000000L //Lieteral Long
    println("Contoh literal Long : " + oneBillion)

    val valHeksa = 0x0f //Lieral heksadesimal diawali dengan 0x
    println("Contoh literal Heksadesimal : " + valHeksa)

    val valBinary = 0b00001011 //Lieral binary diawali dengan 0b
    println("Contoh Lieral Binary : " + valBinary)

    val notasiKonvensional = 123.5e10
    println("Contoh literal Konvensional Floating Floating Point Number : " + notasiKonvensional)


}